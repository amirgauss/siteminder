class tableminder {

	private WebElement elContainer;

	/**
	 * Get Web element of each part of table
	 * .//*[@class="el-table__body"]/tbody/child::tr[1]/
	 * @param table Katalon test object represent for HTML table
	 * @param outerTagName outer tag name of TR tag, usually is TBODY
	 * @return All rows inside HTML table
	 */

	@Keyword
	def String getElementOfTable(TestObject container,String row,String column,String depth){
		this.elContainer = WebUiBuiltInKeywords.findWebElement(container);
		WebElement temp = elContainer.findElement(By.xpath(String.format(".//*[@class='el-table__body']/tbody/child::tr[%s]/td[%s]//span[%s]", row ,column,depth)));
		KeywordUtil.logInfo(temp.getText())
		return temp.getText()
	}
}